export function getArticle(category: string, slug: string, locale: string): { title: string; excerpt: string; content: string } | undefined {
  const categoryArticles = articles[category as keyof typeof articles]
  if (!categoryArticles) return undefined
  
  const slugArticles = categoryArticles[slug as keyof typeof categoryArticles]
  if (!slugArticles) return undefined
  
  const article = slugArticles[locale as keyof typeof slugArticles]
  return article
}

export function getAllArticles() {
  const allArticles: Array<{
    category: string
    slug: string
    locale: string
    title: string
    excerpt: string
  }> = []

  Object.entries(articles).forEach(([category, categoryArticles]) => {
    Object.entries(categoryArticles).forEach(([slug, localeArticles]) => {
      Object.entries(localeArticles).forEach(([locale, article]) => {
        allArticles.push({
          category,
          slug,
          locale,
          title: article.title,
          excerpt: article.excerpt
        })
      })
    })
  })

  return allArticles
}